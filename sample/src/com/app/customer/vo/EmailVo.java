package com.app.customer.vo;

public class EmailVo {
private String from;
private String to;
private String cc;
private String bc;
private String sub;
private String meg;
public String getFrom() {
	return from;
}
public void setFrom(String from) {
	this.from = from;
}
public String getTo() {
	return to;
}
public void setTo(String to) {
	this.to = to;
}
public String getCc() {
	return cc;
}
public void setCc(String cc) {
	this.cc = cc;
}
public String getBc() {
	return bc;
}
public void setBc(String bc) {
	this.bc = bc;
}
public String getSub() {
	return sub;
}
public void setSub(String sub) {
	this.sub = sub;
}
public String getMeg() {
	return meg;
}
public void setMeg(String mes) {
	this.meg = mes;
}
}
